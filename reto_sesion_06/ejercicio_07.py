# ¿El cuadrado y la suma de 2 y 2 son iguales?

"""
Cuadrado de 2:
El cuadrado de un número es el resultado de multiplicar ese número por sí mismo. 
En este caso, el cuadrado de 2 es:22=4

Suma de 2 y 2:

Simplemente sumamos los dos números:2+2=4
Ahora, comparamos los resultados:

El cuadrado de 2 es 4.
La suma de 2 y 2 también es 4.
Por lo tanto, el cuadrado de 2 y la suma de 2 y 2 son iguales. Ambas expresiones dan como 
resultado el número 4.
"""

cuadrado_de_2 = 2 ** 2
suma_de_2_y_2 = 2 + 2

if cuadrado_de_2 == suma_de_2_y_2:
    print("El cuadrado de 2 y la suma de 2 y 2 son iguales.")
else:
    print("El cuadrado de 2 y la suma de 2 y 2 no son iguales.")
