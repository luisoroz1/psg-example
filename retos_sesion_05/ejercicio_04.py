"""Calcular el volumen de un cubo donde uno de sus lados mide 10 metros, utilizando la 
fórmula general del volumen de un cubo"""

# Lado del cubo (en metros)
lado = 10

# Calculamos el volumen del cubo
volumen = lado ** 3

# Mostramos el resultado
print(f"El volumen del cubo con un lado de {lado} metros es: {volumen} metros cúbicos")


